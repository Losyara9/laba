a = int(input())
b = int(input())
c = int(input()) #ввод переменных

d = b**2 - (4*a*c) #формула дискриминанта
if d > 0:
    x1 = (-b + (d ** 0.5))/(2*a) #формула первого корня
    x2 = (-b - (d ** 0.5))/(2*a) #формула второго корня
    print('Корни уравнения: x1 = ', x1, ' x2 = ', x2)
else:
    if d == 0:
        x = -b/(2*a) #формула корня при дискриминанте равном нулю
        print('Корень уравнения - x = ', x)
    else:
        if d < 0:
            print('Корней нет')